// Dimensions and Positioning
console.log(window.innerWidth); // Output: Width of the browser window's content area
console.log(window.innerHeight); // Output: Height of the browser window's content area
console.log(window.outerWidth); // Output: Width of the browser window including toolbar and scrollbar
console.log(window.outerHeight); // Output: Height of the browser window including toolbar and scrollbar
console.log(window.screenX); // Output: X-coordinate of the window relative to the screen
console.log(window.screenY); // Output: Y-coordinate of the window relative to the screen
