// Window State
console.log(window.document); // Output: The document object representing the content of the current window
console.log(window.closed); // Output: Whether the window has been closed
console.log(window.frames.length); // Output: Number of frames in the window
console.log(window.opener); // Output: Reference to the window that opened this window
console.log(window.parent); // Output: Reference to the parent of the current window
