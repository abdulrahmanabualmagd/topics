// Other Properties
console.log(window.name); // Output: Name of the window
console.log(window.frameElement); // Output: The <iframe> element that contains the window
console.log(window.self); // Output: Reference to the window itself
console.log(window.top); // Output: Reference to the topmost window in the window hierarchy
console.log(window.crypto); // Output: Crypto object
console.log(window.performance); // Output: Performance object
console.log(window.screen); // Output: Screen object
