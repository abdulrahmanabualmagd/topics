// Comparison Operators
let x = 5;
let y = 10;
console.log(x == y); // Output: false
console.log(x != y); // Output: true
console.log(x === '5'); // Output: false (strict equality)
console.log(x !== '5'); // Output: true (strict inequality)
console.log(x > y); // Output: false
console.log(x < y); // Output: true